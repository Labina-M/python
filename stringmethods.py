#isalnum()
"""Returns true if string has at least 1
character and every character is
either a number or an alphabet"""

message="JamesBond007"
print(message.isalnum())

#isalpha()
"""Returns true if string has at least 1
character and every character is an
alphabet"""

message="JamesBond007"
print(message.isalpha())


#isdigit() 
"""Returns true if string has at least 1
character and every character is a
digit"""

message="007"
print(message.isdigit())

#islower()
"""Returns true if string has at least 1
character and every character is a
lowercase alphabet"""

message="Hello"
print(message.islower())

#isupper() 
"""Returns true if string has at least 1
character and every character is an
uppercase alphabet"""

message="HELLO"
print(message.isupper())

#lower() 
"""Converts all characters in the string
into lowercase"""

str="Hello"
print(str.lower())

#upper() 
"""Converts all characters in the string
into uppercase"""

str="Hello"
print(str.upper())


#strip() 
